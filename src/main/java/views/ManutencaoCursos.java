    public void atualizarDados(){
        DefaultTableModel model =(DefaultTableModel) jTable1.getModel();
        model.setNumRows(0);
        
        Curso curso = new Curso();
        ResultSet cursos = curso.getCursos();
        
        try {
            while(cursos.next()){
                model.addRow(new Object[]{
                    cursos.getInt("codigo"),
                    cursos.getString("descricao"),
                    cursos.getString("ementa")
                });
            }
        } catch (SQLException ex) {
            Logger.getLogger(ManutencaoCursos.class.getName()).log(Level.SEVERE, null, ex);
        }        
    }

