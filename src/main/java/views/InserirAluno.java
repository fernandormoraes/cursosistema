    private void btnInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirActionPerformed
        boolean cursoExiste;
        
        cursoExiste = verificaCurso();
        
        if(cursoExiste){
            ControllerAluno com = new ControllerAluno();
            Aluno aluno = new Aluno();
            aluno.setNome(txtNome.getText());
            aluno.setCodigo(Codigo);
            aluno.setCodigoCurso(Integer.parseInt(txtCodigoCurso.getText()));

            if(Inserindo == true){
                try {
                    com.insereAluno(aluno);
                } catch (SQLException ex) {
                    Logger.getLogger(InserirCurso.class.getName()).log(Level.SEVERE, null, ex);
                }
            }else{
                try {
                    com.alteraAluno(aluno);
                } catch (SQLException ex) {
                    Logger.getLogger(InserirCurso.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            setVisible(false);
            dispose();
        }else{
            JOptionPane.showMessageDialog (null, "Falha ao inserir aluno!");
        }
    }//GEN-LAST:event_btnInserirActionPerformed

