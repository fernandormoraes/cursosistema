    public boolean verificaCurso(){
        Aluno aluno = new Aluno();

        try {
            String DescricaoCurso;
            DescricaoCurso = aluno.getDescricaoCurso(Integer.parseInt(txtCodigoCurso.getText()));
            if(DescricaoCurso.equals("")){
                JOptionPane.showMessageDialog (null, "Não existe o código de curso informado!");
                txtCodigoCurso.requestFocus();
                return false;
            }else{
                lblCursoDescricao.setText(DescricaoCurso);
                return true;
            }
        } catch (SQLException ex) {
            Logger.getLogger(InserirAluno.class.getName()).log(Level.SEVERE, null, ex);
        }      
        return false;
    }

